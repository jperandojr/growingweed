// Any image dropped into /public/strains named {slug}.jpg|png|webp is
// automatically attached to its strain — no data edits needed. Additional
// angles named {slug}-2, {slug}-3, {slug}-4 become the strain's gallery.
function loadImageManifest(): Map<string, string[]> {
  const dir = path.join(process.cwd(), "public", "strains");
  const bySlug = new Map<string, { order: number; src: string }[]>();
  try {
    for (const file of fs.readdirSync(dir)) {
      const m = file.match(/^(.+?)(?:-([2-9]))?\.(jpg|jpeg|png|webp)$/i);
      if (!m) continue;
      const slug = m[1];
      const order = m[2] ? parseInt(m[2], 10) : 1;
      if (!bySlug.has(slug)) bySlug.set(slug, []);
      bySlug.get(slug)!.push({ order, src: `/strains/${file}` });
    }
  } catch {
    // directory doesn't exist yet — no images
  }
  const manifest = new Map<string, string[]>();
  for (const [slug, entries] of bySlug) {
    manifest.set(
      slug,
      entries.sort((a, b) => a.order - b.order).map((e) => e.src)
    );
  }
  return manifest;
}

